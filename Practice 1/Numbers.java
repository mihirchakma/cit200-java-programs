/* Converting Octal, Hexadecimal and binary numbers to Decimal in Java */

public class Numbers {
    public static void main(String[] args) {
        System.out.println("Decimal value of Octal 73 is " + 073);         // Decimal value of Octal 73 is 59
        System.out.println("Decimal value of Hexa-decimal AC is " + 0xAC); // Decimal value of Hexa-decimal AC is 172
        System.out.println("Decimal value of Binary 10110 is " + 0b10110); // Decimal value of Binary 10110 is 22
    }
}
