public class Literals {
    public static void main(String[] args) {
        System.out.println(123);                  // 123 is a byte literal
        System.out.println(2047483647);           // 2047483647 is an integer literal
        System.out.println(9223372036854775807L); // 9223372036854775807 is a long literal
        System.out.println(13.5F);                // 13.5 float is a literal
        System.out.println(107.5);                // 107.5 is a double literal
        System.out.println('A');                  // A is a char literal
        System.out.println(true);                 // true is a boolean literal
        System.out.println(false);                // false is a boolean litera
    }
}
