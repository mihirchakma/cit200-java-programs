// Converting a String to an Integer object 

public class Parse4 {
    public static void main(String[] args) {
        String strVal = "135";

        Integer iObjVal = Integer.parseInt(strVal);
        System.out.println(strVal);
        System.out.println(iObjVal);
        System.out.println(iObjVal * 2);
    }
}
